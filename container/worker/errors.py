"""
Кастомные исключения для воркера парсинга Авито

Используются для различения типов ошибок и правильной обработки в главном цикле
"""


class ProxyBlockedError(Exception):
    """
    Прокси заблокирован Авито (403/407)

    Требуется блокировка прокси в БД и взятие нового
    """
    pass


class CaptchaNotSolvedError(Exception):
    """
    Капча не решена после максимального количества попыток

    Требуется смена прокси и повторная попытка
    """
    pass


class NoProxiesAvailableError(Exception):
    """
    Нет свободных прокси в базе данных

    Воркер должен вернуть задачу в очередь и ждать появления прокси
    """
    pass


class NoTasksAvailableError(Exception):
    """
    Нет задач в очереди

    Воркер должен подождать и повторить попытку
    """
    pass
